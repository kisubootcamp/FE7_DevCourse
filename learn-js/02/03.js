// 산술 연산자
// 수학적인 계산을 할 깨 사용하는 연산자
// 덧셈, 뺼셈, 곱셈, 나눗셈, 나머지, 제곱

console.log(10 + 5); // 15
console.log(10 - 5); // 5
console.log(10 * 5); // 50
console.log(10 / 5); // 2
console.log(10 % 5); // 2
console.log(10 ** 5); // 100000


// 연산자 우선순위
// 소괄호
console.log(10 + 5 * 2); // 20
console.log((10 + 5) * 2); // 30

// Decimal.js
console.log(0.1 * 100000000)