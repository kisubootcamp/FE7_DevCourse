// 대입 연산자
// 할당 연산자, 복합 대입 연산자
// 할당 연산자 (=)
const num = 10;

// 복합 대입 연산자(산술 연산자 + 대입 연산자)
// +=, -=, *=, /=, %=, **=
let x = 10;
x += 5; // x = x + 5; // 15
x -= 3; // x = x - 3; // 12
x *= 2; // x = x * 2; // 24
x /= 4; // x = x / 4; // 6
x %= 5; // x = x % 5; // 1
x **= 3; // x = x ** 3; // 1
console.log(x); // 1
// 복합 대입 연산자는 변수에 값을 누적할 때 유용하게 사용됨
