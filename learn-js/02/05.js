// 대입 연산자
// 할당 연산자, 복합 대입 연산자

// 할당 연산자 (=)
const num = 10; // 우항에 있는 값을 좌항에 할당

// 복합 대입 연산자 (산술 연산자 + 대입 연산자)
// +=, -=, *=, /, %=, **=

let x = 10;
x += 10; // x = x + 10; 와 같음
x -= 10; // x = x - 10; 와 같음
x *= 10; // x = x * 10; 와 같음
x /= 10; // x = x / 10; 와 같음
x %= 10; // x = x % 10; 와 같음
x **= 2; // x = x ** 2; 와 같음

console.log(x);

