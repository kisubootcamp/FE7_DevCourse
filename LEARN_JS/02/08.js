// 1. 논리연산자
// 1.1 논리곱 (AND)
// 1.1.1 논리곱 연산자(&&)
// 1.1.2 논리곱 연산자 사용 예시
let a = true;
let b = false;
let resultAnd = a && b; // false
console.log(`논리곱 결과: ${resultAnd}`); // 논리곱 결과: false

// 1.2 논리합 (OR)
// 1.2.1 논리합 연산자(||)
// 1.2.2 논리합 연산자 사용 예시
let c = true;
let d = false;
let resultOr = c || d; // true
console.log(`논리합 결과: ${resultOr}`); // 논리합 결과: true
