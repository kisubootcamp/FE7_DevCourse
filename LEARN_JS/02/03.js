// 산술 연산자
// 수학적인 계산을 수행하는 연산자
// 덧셈(+), 뺄셈(-), 곱셈(*), 나눗셈(/), 나머지(%), 제곱(**)
console.log(10 + 5); // 15
console.log(10 - 5); // 5
console.log(10 * 5); // 50
console.log(10 / 5); // 2
console.log(10 % 3); // 1
console.log(2 ** 3); // 8

// 연산자 우선순위
// 소괄호
console.log(2 + 5 * 2); // 12 (곱셈이 덧셈보다 우선)
console.log((2 + 5) * 2); // 14 (소괄호가 우선)

console.log(0.1 + 0.9); // 1.0 (부동소수점 오차)
console.log(0.1 + 0.2); // 0.30000000000000004 (부동소수점 오차)
